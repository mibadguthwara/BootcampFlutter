void main(List<String> args) {
  // string tipe data karakter
  final String name = "M Ibad Guthwara";
  print(name);

  // tipe data number atau angka
  final int angka1 = 90;
  print(angka1);

  // doubel atau desimal
  final double angka2 = 93.2;
  print(angka2);

  // tipe data bool true dan false
  final bool isLoading = true;
  print(isLoading);
}
